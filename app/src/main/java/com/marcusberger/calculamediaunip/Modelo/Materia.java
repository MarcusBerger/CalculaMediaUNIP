package com.marcusberger.calculamediaunip.Modelo;

public class Materia {

    private String nome;
    private  String professor;
    private String notaP1;
    private String notaP2;
    private String notaExame;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getProfessor() {
        return professor;
    }

    public void setProfessor(String professor) {
        this.professor = professor;
    }

    public String getNotaP1() {
        return notaP1;
    }

    public void setNotaP1(String notaP1) {
        this.notaP1 = notaP1;
    }

    public String getNotaP2() {
        return notaP2;
    }

    public void setNotaP2(String notaP2) {
        this.notaP2 = notaP2;
    }

    public String getNotaExame() {
        return notaExame;
    }

    public void setNotaExame(String notaExame) {
        this.notaExame = notaExame;
    }
}
